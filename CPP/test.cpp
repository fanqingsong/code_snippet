const int MAX_N = 1 << 20;
int H[MAX_N + 1];

int main(){
	int n;
	
	for (int i = 0; i <= 20; ++i) H[1 << i] = i;
	
	while (cin >> n) {
	    while (n > 0) {
	        cout << H[n & -n] << ' ';
	        n -= n & -n;
	    }
	    
	    cout << endl;
	}
}

